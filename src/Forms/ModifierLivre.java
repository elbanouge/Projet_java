    private void jButtonModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModActionPerformed
        // TODO add your handling code here:

        String isbn = jTextFieldISBN.getText();
        String titre = jTextFieldTitre.getText();
        String langue = jTextFieldLang.getText();
        int annee = Integer.parseInt(jTextFieldAnnee.getText());
        int nb_pages = Integer.parseInt(jSpinnerNmPages.getValue().toString());
        int nb_exmpl = Integer.parseInt(jSpinnerNmExmp.getValue().toString());
        double prix = Double.parseDouble(jTextFieldPrix.getText());
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dateFormat.format(jDateChooserDate.getDate());
        String desc = jTextAreaDesc.getText();
        String[] sTheme = jComboBoxTheme.getSelectedItem().toString().split(" - ");
        int theme = Integer.parseInt(sTheme[0]);
        String[] sAuteur = jTextFieldAut.getText().split(" - ");
        int auteur = Integer.parseInt(sAuteur[0]);
        Livre l = livre.getLivreById(Integer.parseInt(isbn), isbn);

        if (isbn.isEmpty()) {
            jLabelError.setText("* Entrer ISBN de livre");
        } else if (titre.isEmpty()) {
            jLabelError.setText("* Entrer titre de livre");
        } else if (langue.isEmpty()) {
            jLabelError.setText("* Entrer langue de livre");
        } else if (nb_pages == 0) {
            jLabelError.setText("* Entrer nombre de page de livre");
        } else if (nb_exmpl == 0) {
            jLabelError.setText("* Entrer nombre d'exemplaire de livre");
        } else {

            byte[] imageA = null;
            try {

                if (ImagePath != null) {
                    Path path = Paths.get(ImagePath);
                    imageA = Files.readAllBytes(path);
                } else {
                    imageA = img;
                }

                livre.Modifier(l.getIdL(), isbn, auteur, theme, titre, langue, annee, nb_pages, nb_exmpl, prix, date, desc, img);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Attention", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonModActionPerformed

