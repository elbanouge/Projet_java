    private void jButtonIRchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIRchActionPerformed
        // TODO add your handling code here:
        try {
            String isbn_ID = jTextFieldISBN.getText();
            Livre l = livre.getLivreById(Integer.parseInt(isbn_ID), isbn_ID);

            if (l != null) {
                jTextFieldTitre.setText(l.getTitre());
                jTextFieldLang.setText(l.getLangue());
                jTextFieldAnnee.setText(l.getAnnee() + "");
                jSpinnerNmPages.setValue(l.getNbr_pages());
                jSpinnerNmExmp.setValue(l.getNbr_exemp());
                jTextFieldPrix.setText(l.getPrix() + "");

                Date date = dateFormat.parse(l.getDate_achat());
                jDateChooserDate.setDate(date);

                for (String name : mapTh.keySet()) {

                    String key = name;
                    String value = mapTh.get(name).toString();
                    if (value.equals(String.valueOf(l.getTheme()))) {
                        jComboBoxTheme.setSelectedItem(value + " - " + key);
                    }
                }

                Auteur aut = new Auteur();
                Auteur a = aut.getAutByID(l.getAuteur());
                jTextFieldAut.setText(String.valueOf(l.getAuteur()) + " - " + a.getNom() + " " + a.getPrenom());
                jTextAreaDesc.setText(l.getDesc());

                img = l.getImage();
                f.DisplayIcon(jLabelImg.getWidth(), jLabelImg.getHeight(), img, "", jLabelImg);
                jLabelImage.setText(ImagePath);

            } else {
                JOptionPane.showMessageDialog(null, "Ce livre n'existe pas", "Attention", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException | ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Attention", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonIRchActionPerformed

