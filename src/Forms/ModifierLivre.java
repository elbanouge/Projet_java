    private void jButtonSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSuppActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String idLv = jTextFieldISBN.getText();
            String isbnLv = jTextFieldISBN.getText();

            int confirm = JOptionPane.showConfirmDialog(null, "Vous voullez supprime l'adherent");

            if (confirm == JOptionPane.YES_OPTION) {

                if (f.TryParseInt(idLv) != null && idLv.length() < 3) {
                    livre.Supprimer(idLv, "E.ID_LIVRE");
                    Vider();
                } else {
                    livre.Supprimer(isbnLv, "L.ISBNLV");
                    Vider();
                }
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Attention", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSuppActionPerformed

