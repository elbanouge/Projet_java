    private void jButtonRchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRchActionPerformed
        try {
            // TODO add your handling code here:
            Adherent a = null;
            
            String CNEIDAhr = jTextFieldCNEA.getText();
            if (f.TryParseInt(CNEIDAhr) != null && CNEIDAhr.length() < 3) {
                a = adh.getAdherentById(null, Integer.parseInt(CNEIDAhr));
            }else{
                a = adh.getAdherentById(CNEIDAhr, 0);
            }

            if (a != null) {

                jTextFieldNomA.setText(a.getNom());
                jTextFieldPrenomA.setText(a.getPrenom());

                Date date = dateFormat.parse(a.getDateNaiss());
                jDateChooserDateNaiss.setDate(date);
                jTextFieldEmailA.setText(a.getEmail());
                jTextFieldTelA.setText(a.getTel());

                if (a.getSexe() == 1) {
                    jComboBoxSexe.setSelectedItem("Homme");
                } else {
                    jComboBoxSexe.setSelectedItem("Femme");
                }

                img = a.getImage();

                f.DisplayIcon(jLabelImg.getWidth(), jLabelImg.getHeight(), img, "", jLabelImg);
                jLabelImage.setText(ImagePath);
            } else {
                JOptionPane.showMessageDialog(null, "L'adherent n'existe pas", "Attention", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException | ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Attention", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonRchActionPerformed

